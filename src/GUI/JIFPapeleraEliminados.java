    private void BTRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTRestaurarActionPerformed
        this.index = this.JLClienteIna.getSelectedIndex();
        int respuesta  = JOptionPane.showConfirmDialog(null, "¿Estas seguro que deseas restaurar?", "Mensaje de Confirmación", JOptionPane.YES_NO_OPTION);
        Object objC = this.JLClienteIna.getSelectedValue();
        Cliente cliente = (Cliente) objC;
        try{
            
            if(respuesta == 0){
                
                this.modelolista.remove(index);
                BDGestionarClientes gstC = new BDGestionarClientes();
                this.index = cliente.getId();
                gstC.restaurar(this.index);
                
            }
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }finally{
            this.index = -1;
        }
    }//GEN-LAST:event_BTRestaurarActionPerformed

