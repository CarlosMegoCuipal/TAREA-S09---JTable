    public void CargarData(){
        BDGestionarClientes bdCliente = new BDGestionarClientes();
        try{
            List listado = bdCliente.listarinabilitados();
            for(Object object : listado){
                Cliente c = (Cliente) object;
                this.modelolista.addElement(c);
            }
        }catch(Exception e){
            
        }
    }
