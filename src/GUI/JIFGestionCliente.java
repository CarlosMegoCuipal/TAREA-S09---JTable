    private void cargarData() throws Exception {
        try{
            BDGestionarClientes gestC = new BDGestionarClientes();
            List listado = gestC.listar();
            for(Object object : listado){
                Cliente c = (Cliente) object;
                this.listmodeloCliente.addElement(c);
            }
            System.out.println("Listado Exitoso");
        }catch(Exception e){
            throw e;
        }
    }
