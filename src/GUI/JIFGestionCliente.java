    private void BTGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTGuardarActionPerformed
        Cliente objC = new Cliente();
        BDGestionarClientes gestC = new BDGestionarClientes();
        try{
            validarCliente();
            objC.setApellido(this.TFApellido.getText());
            objC.setNombre(this.TFNombre.getText());
            objC.setDni(this.TFDni.getText());
            objC.setCorreo(this.TFCorreo.getText());
            objC.setDireccion(this.TFDireccion.getText());
            objC.setTelef(this.TFTelefono.getText());
            
            if(!this.estEditando){
            
               int id = gestC.crear(objC);
               objC.setId(id);
               System.out.println("id: " + id);
               this.listmodeloCliente.addElement(objC);
            
            }
            else{
               
                Cliente cliEdit = (Cliente) this.listmodeloCliente.get(this.index);
                gestC.actualizar(cliEdit.getId(), objC);
                this.listmodeloCliente.set(index, objC);
                this.BTNuevo.setEnabled(true);
            
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            this.BTCancelarActionPerformed(evt);
        }finally{
            limpiar();
            this.HabilitarDesabilitarTF(false);
            this.habilitarBotonesDerecha(false);
            this.habilitarBotonesIzquierda(false);
            this.estEditando = false;
            this.index = -1;
        }
    }//GEN-LAST:event_BTGuardarActionPerformed

    private void TFTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFTelefonoKeyTyped
        int cantLetras = this.TFTelefono.getText().length();
        char c = evt.getKeyChar();
        validarNumero(evt, 9, cantLetras);
        if (this.TFTelefono.getText().length() == 0 && c != '9') {
            evt.consume();
        }
    }//GEN-LAST:event_TFTelefonoKeyTyped

