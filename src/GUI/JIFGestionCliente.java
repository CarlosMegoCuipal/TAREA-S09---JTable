    private void BTEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTEliminarActionPerformed
        int respuesta  = JOptionPane.showConfirmDialog(null, "¿Estas seguro que deseas eliminar?", "Mensaje de Confirmación", JOptionPane.YES_NO_OPTION);
        this.index = this.JLCliente.getSelectedIndex();
        Object objC = this.JLCliente.getSelectedValue();
        Cliente cliente = (Cliente) objC;
        //Cliente.setEstado(0);
        if(objC == null){
            return;
        }
        try{
            
            if(respuesta == 0){
                
                this.listmodeloCliente.remove(index);
                BDGestionarClientes gstC = new BDGestionarClientes();
                this.index = cliente.getId();
                gstC.eliminar(this.index);
                
            }
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }finally{
            this.index = -1;
            this.habilitarBotonesDerecha(false);
            this.habilitarBotonesIzquierda(false);
            this.limpiar();
        }
        
    }//GEN-LAST:event_BTEliminarActionPerformed

